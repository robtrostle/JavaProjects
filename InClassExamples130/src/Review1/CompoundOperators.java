public class CompoundOperators {

  
    public static void main(String[] args) {
        int num = 10;
        
        System.out.println("Num has the value of " + num);
        
        num +=5;
        //AKA num = num + 5;
        
        System.out.println("Num has the value of " + num);
        
        num -= 5;
        
        System.out.println("Num has the value of " + num);
        
        num /= 5;
        
        System.out.println("Num has the value of " + num);
    }
    
}
